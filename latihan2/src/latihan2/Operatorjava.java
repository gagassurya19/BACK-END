public class Operatorjava {
    public static void main(String[] args) {
        
        int a = 12;
        int b = 5;
        
        int c;
        
        System.out.println("=======]Bilangan bulat[=======");
        //Penjumlahan
        c = a + b;
        System.out.println(a + " + " + b + " = " + c); 
        
        //Pengurangan
        c = a - b;
        System.out.println(a + " - " + b + " = " + c);
        
        //Perkalian
        c = a * b;
        System.out.println(a + " X " + b + " = " + c);
        
        //pembagian
        c = a / b;
        System.out.println(a + " / " + b + " = " + c);
        
        //hasil bagi %
        c = a % b;
        System.out.println(a + " % " + b + " = " + c);
        
        System.out.println("=======]Bilangan bulat[=======");
           
        
        System.out.println("");
    
        
        //Bilangan Desimal        
        double d = 9.3;
        double e = 4.7;
        
        double f;
        
        System.out.println("======]Bilangan Desimal[======");
                
        //Perkalian
        f = d * e;
        System.out.println(d + " X " + e + " = " + f);
        
        //pembagian
        f = d / e;
        System.out.println(d + " / " + e + " = " + f);
        
        //hasil bagi %
        f = d % e;
        System.out.println(d + " % " + e + " = " + f);
        
        System.out.println("======]Bilangan Desimal[======");
        
        System.out.println("");
        
    }
}
