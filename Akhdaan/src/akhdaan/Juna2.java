package akhdaan;

public class Juna2 {
    public static void main(String[] args){
        // Deklarasi //
        int a = 10;
        int b = 2;
        int c;
        
        // Penjumlahan //
        c = a + b;
        System.out.println("a + b = c");
        System.out.println("Hasil : " + c);
        
        // Pengurangan //
        c = a - b;
        System.out.println("a - b = c");
        System.out.println("Hasil : " + c);
        
        // Perkalian //
        c = a * b;
        System.out.println("a * b = c");
        System.out.println("Hasil : " + c);
        
        // Pembagian //
        c = a / b;
        System.out.println("a / b = c");
        System.out.println("Hasil : " + c);
    }
}
